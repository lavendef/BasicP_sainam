rayatann = int(input("คุณพี่จะส่งเท่าไรค่ะ :"))
if 5 <= rayatann <= 50:
    print("ราคาค่าส่ง = 10 บาทค่ะ")
elif 51 <= rayatann <= 100:
    print("ราคาค่าส่ง = 15 บาทค่ะ")
elif 101 <= rayatann <= 300:
    print("ราคาค่าส่ง = 25 บาทค่ะ")
elif 301 <= rayatann <= 500:
    print("ราคาค่าส่ง = 35 บาทค่ะ")
else:
    print("ราคาค่าส่ง = 45 บาทค่ะ")
